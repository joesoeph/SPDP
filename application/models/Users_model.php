    function RoleSelected($id = NULL, $tabel = NULL, $pk = NULL){
      $sql = "SELECT a.RoleId, a.RoleName, CASE WHEN (b.role IS NOT NULL) THEN \"selected = 'selected'\" ELSE 'null' END AS selected
              FROM roles a LEFT JOIN (SELECT role
              FROM $tabel WHERE $pk='$id') b ON a.RoleId = b.role
              WHERE a.Active = 1";

      return $this->db->query($sql)->result();
