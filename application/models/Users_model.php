    function getDataJabatan($id = NULL)
    {
      $sql = "SELECT a.JabatanId, a.JabatanCode, a.JabatanName,
                CASE WHEN (b.JabatanId IS NOT NULL) THEN \"selected = 'selected'\" ELSE 'null' END AS selected
              FROM mstjabatan a 
              LEFT JOIN (SELECT JabatanId
                FROM users WHERE id='".$id."') b ON a.JabatanId = b.JabatanId
              WHERE a.DeletedDate IS NULL";

      return $this->db->query($sql)->result();
    }

